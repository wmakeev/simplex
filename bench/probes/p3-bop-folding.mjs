// P3 (B8, H7) — is the bop["+"] operator-table lookup foldable on the REAL
// codegen output?
//
// Mechanism per research B8: Function Context Specialization. Preconditions
// visible in bytecode of the generated `data => …` arrow:
//   - `bop` is read via an (immutable) context slot load
//     (LdaImmutableCurrentContextSlot / LdaImmutableContextSlot) — i.e. the
//     bootstrap `var bop = ctx.binaryOperators` is context-allocated and
//     never reassigned;
//   - the "+" lookup is a GetNamedProperty with a feedback slot (IC), which
//     TurboFan + FCS can constant-fold when the context is a compile-time
//     constant (closure compiled per-compile()-call).
// The probe extracts the bytecode of the generated arrow (identified by a
// unique Smi constant in the expression) and records these markers.
// Full TurboFan IR verification (--trace-turbo) is recorded as
// available/not-available; the actual folding payoff is measured by the
// harness itself (front 01), not asserted here.
//
// Also records (H7): Turbolev default state on this build.
//
// Run: node bench/probes/p3-bop-folding.mjs

import { execSync } from 'node:child_process'
import { buildIndexUrl, runNode, section } from './lib.mjs'

const MARKER = 424243 // unique Smi that identifies our generated function

const childCode = `
  const { compile } = await import(${JSON.stringify(buildIndexUrl)});
  const fn = compile('a + ${MARKER}');
  console.log('RESULT', fn({ a: 1 }));
`

section('1. bytecode of the generated arrow (--print-bytecode)')

const res = runNode(
  ['--print-bytecode', '--print-bytecode-filter=*'],
  childCode
)
if (res.status !== 0) throw new Error(`child failed: ${res.stderr}`)

// Bytecode listings are separated by blank lines; find the one whose
// constant pool / bytecode mentions our marker Smi.
const blocks = res.stdout.split(/\n(?=\[generated bytecode)/)
const ours = blocks.filter(b => b.includes(String(MARKER)))
console.log(`bytecode blocks total: ${blocks.length}, with marker: ${ours.length}`)

let contextSlotLoad = null
let plusLookup = null
for (const block of ours) {
  const header = block.split('\n')[0]
  console.log(`\n--- ${header}`)
  for (const line of block.split('\n')) {
    if (/Lda(Immutable)?(Current)?ContextSlot/.test(line)) {
      if (!contextSlotLoad) contextSlotLoad = line.trim()
    }
    if (/GetNamedProperty|GetKeyedProperty/.test(line)) {
      if (!plusLookup) plusLookup = line.trim()
    }
    if (
      /ContextSlot|GetNamedProperty|GetKeyedProperty|CallProperty|LdaSmi/.test(
        line
      ) &&
      !/^\s*$/.test(line)
    ) {
      console.log(' ', line.trim().replace(/\s+/g, ' ').slice(0, 110))
    }
  }
}

section('2. verdict on bytecode markers')
console.log(
  JSON.stringify(
    {
      immutableContextSlotLoad:
        contextSlotLoad !== null && contextSlotLoad.includes('Immutable'),
      contextSlotLoadExample: contextSlotLoad,
      plusLookupExample: plusLookup
    },
    null,
    2
  )
)

section('3. --trace-turbo availability (deep IR check, optional)')
const tt = runNode(
  ['--allow-natives-syntax', '--trace-turbo', '--trace-turbo-path=/tmp'],
  `function f(a){return a+1}
   %PrepareFunctionForOptimization(f);
   f(1); f(2);
   %OptimizeFunctionOnNextCall(f);
   f(3);
   console.log('ok');`
)
console.log(
  `exit=${tt.status}; stdout mentions turbo files: ${/turbo/i.test(tt.stdout + tt.stderr)}`
)
console.log((tt.stdout + tt.stderr).split('\n').filter(l => /turbo|\.cfg|\.json/i.test(l)).slice(0, 5).join('\n'))

section('4. Turbolev default (H7)')
const opts = execSync(`${process.execPath} --v8-options`, { encoding: 'utf8' })
const turbolev = opts.match(/--turbolev \(.*?\)\n\s+type: bool\s+default: (\S+)/)
console.log(`--turbolev default: ${turbolev ? turbolev[1] : 'flag not found'}`)
